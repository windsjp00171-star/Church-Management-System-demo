@tutorial_bp.get('/tutorial')
def tutorial():
    if not session.get('user_id'):
        return redirect(url_for('auth.login_page', next='/tutorial'))
    lessons = _build_lessons()
    sections = []
    for sec in ('前台', '後台'):
        items = [l for l in lessons if l['section'] == sec]
        if items:
            sections.append({'name': sec, 'mods': items})
    return render_template('tutorial.html', lessons=lessons, sections=sections)
